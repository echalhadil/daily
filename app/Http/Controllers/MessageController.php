    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        try {

            $conversation = Conversation::find($request -> conversation_id ); // find the conversation .

            $message = $conversation -> messages() -> create([
                'text'      => $request ->  text,
                'readed'    => false,
                'user_id'   =>  Auth::user() -> id ,
            ]); // store the message conversation.

            $conversation -> last_message = $request -> text; // update lest message info
            $conversation -> save();
            return response() -> json($message);
            

        } catch (\Throwable $th) {
            throw $th;
        }
    }

